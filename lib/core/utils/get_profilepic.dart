    case "nobara":
      profilePic = "assets/profilepics/nobara.jpg";
    case "todo":
      profilePic = "assets/profilepics/todo.jpg";
    case "takuma":
      profilePic = "assets/profilepics/ino.jpg";
    case "kinji":
      profilePic = "assets/profilepics/hakari.jpg";
    case "yuta":
      profilePic = "assets/profilepics/yuta.jpeg";
    case "megumi":
      profilePic = "assets/profilepics/megumi.jpg";
    case "geto":
      profilePic = "assets/profilepics/geto.jpg";
    case "shoko":
      profilePic = "assets/profilepics/shoko.jpeg";
    case "satoru":
      profilePic = "assets/profilepics/satoru.jpg";
  }
  return profilePic;
}
